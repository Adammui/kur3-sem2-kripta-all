class Array
  def pretty
    self.map { |v| '%02X' % v }.join('-')
  end

  # shifts these bytes
  def shift_rows
    [0, 5, 10, 15, 4, 9, 14, 3, 8, 13, 2, 7, 12, 1, 6, 11].map { |i| self[i] }
  end

  def shift_rows_inv
    [0, 13, 10, 7, 4, 1, 14, 11, 8, 5, 2, 15, 12, 9, 6, 3].map { |i| self[i] }
  end

  def sub_bytes_inv
    map(&:sboxinv)
  end

  def sub_bytes
    map(&:sbox)
  end

  # http://en.wikipedia.org/wiki/Rijndael_mix_columns
  def mix_col
    a0, a1, a2, a3 = self
    [ a0.g2 ^ a1.g3 ^ a2    ^ a3,    # r0 = 2a0 + 3a1 + 1a2 + 1a3
      a0    ^ a1.g2 ^ a2.g3 ^ a3,    # r1 = 1a0 + 2a1 + 3a2 + 1a3
      a0    ^ a1    ^ a2.g2 ^ a3.g3, # r2 = 1a0 + 1a1 + 2a2 + 3a3
      a0.g3 ^ a1    ^ a2    ^ a3.g2] # r3 = 3a0 + 1a1 + 1a2 + 2a3
  end

  def mix_cols
    self[0, 4].mix_col +
      self[4, 4].mix_col +
      self[8, 4].mix_col +
      self[12, 4].mix_col
  end

  # http://en.wikipedia.org/wiki/Rijndael_mix_columns
  def mix_col_inv
    a0, a1, a2, a3 = self
    [ a0.g14 ^ a3.g9 ^ a2.g13 ^ a1.g11, # r0 = 14a0 + 9a3 + 13a2 + 11a1
      a1.g14 ^ a0.g9 ^ a3.g13 ^ a2.g11, # r1 = 14a1 + 9a0 + 13a3 + 11a2
      a2.g14 ^ a1.g9 ^ a0.g13 ^ a3.g11, # r2 = 14a2 + 9a1 + 13a0 + 11a3
      a3.g14 ^ a2.g9 ^ a1.g13 ^ a0.g11] # r3 = 14a3 + 9a2 + 13a1 + 11a0
  end

  # split into columns, running the matrix against each col
  def mix_cols_inv
    self[0, 4].mix_col_inv +
      self[4, 4].mix_col_inv +
      self[8, 4].mix_col_inv +
      self[12, 4].mix_col_inv
  end

  # See http://en.wikipedia.org/wiki/Rijndael_key_schedule#Key_schedule_core
  def key_schedule_core(i)
    # The input is a 32-bit word and an iteration number i. The output is a 32-bit word.
    # Copy the input over to the output.
    o = self[1, 3] + self[0, 1] # Use the above described rotate operation to rotate the output eight bits to the left
    o = o.sub_bytes # Apply Rijndael's S-box on all four individual bytes in the output word
    o[0] ^= i.rcon # On just the first (leftmost) byte of the output word, exclusive or the byte with 2 to the power of (i-1). In other words, perform the rcon operation with i as the input, and exclusive or the rcon output with the first byte of the output word
    o
  end

  def expand_key
    # http://en.wikipedia.org/wiki/Rijndael_key_schedule#The_key_schedule
    n = self.length # n has a value of 16 for 128-bit keys, 24 for 192-bit keys, and 32 for 256-bit keys
    b = 112 + 4 * n # b has a value of 176 for 128-bit keys, 208 for 192-bit keys, and 240 for 256-bit keys

    # Rijndael's key schedule is done as follows:
    key = self[0, n] # 1. The first n bytes of the expanded key are simply the encryption key
    i = 1 # 2. The rcon iteration value i is set to 1
    until key.length == b do # 3. Until we have b bytes of expanded key, we do the following to generate n more bytes of expanded key:
      # We do the following to create 4 bytes of expanded key:
      t = [] # 1. We create a 4-byte temporary variable, t
      t = key[-4, 4] # 2. We assign the value of the previous four bytes in the expanded key to t
      t = t.key_schedule_core(i) # 3. We perform the key schedule core (see above) on t, with i as the rcon iteration value
      i += 1 # 4. We increment i by 1
      key += t.xor(key[-n, 4]) # 5. We exclusive-or t with the four-byte block n bytes before the new expanded key. This becomes the next 4 bytes in the expanded key
      3.times do # We then do the following three times to create the next twelve bytes of expanded key:
        t = key[-4, 4] # We assign the value of the previous 4 bytes in the expanded key to t
        key += t.xor(key[-n, 4]) # We exclusive-or t with the four-byte block n bytes before the new expanded key. This becomes the next 4 bytes in the expanded key
      end
      if n == 32 # If we are generating a 256-bit key, we do the following to generate the next 4 bytes of expanded key:
        t = key[-4, 4] # We assign the value of the previous 4 bytes in the expanded key to t
        t = t.sub_bytes # We run each of the 4 bytes in t through Rijndael's S-box
        key += t.xor(key[-32, 4]) # We exclusive-or t with the 4-byte block 32 bytes before the new expanded key. This becomes the next 4 bytes in the expanded key.
      end
      reps = 0; reps = 2 if n == 24; reps = 3 if n == 32 # If we are generating a 128-bit key, we do not perform the following steps. If we are generating a 192-bit key, we run the following steps twice. If we are generating a 256-bit key, we run the following steps three times:
      reps.times do
        t = key[-4, 4] # We assign the value of the previous 4 bytes in the expanded key to t
        key += t.xor(key[-n, 4]) # We exclusive-or t with the four-byte block n bytes before the new expanded key. This becomes the next 4 bytes in the expanded key
      end
    end
    key
  end

  # xor's another array
  def xor(other)
    out = []
    self.zip(other) { |a, b| out << (a ^ b) }
    out
  end

  def xor_round_key(keys, i)
    xor(keys[i * 16, 16])
  end
end

def aes_encrypt_128(m, keys)
  # http://en.wikipedia.org/wiki/Advanced_Encryption_Standard#High-level_description_of_the_algorithm
  # Initial Round
  out = m.xor_round_key(keys, 0) # 1. AddRoundKey�each byte of the state is combined with the round key using bitwise xor
  # Rounds
  # AES has 10 rounds for 128-bit keys, 12 rounds for 192-bit keys, and 14 rounds for 256-bit keys
  9.times do |i|
    out = out.sub_bytes # 1. SubBytes�a non-linear substitution step where each byte is replaced with another according to a lookup table.
    out = out.shift_rows # 2. ShiftRows�a transposition step where each row of the state is shifted cyclically a certain number of steps.
    out = out.mix_cols # 3. MixColumns�a mixing operation which operates on the columns of the state, combining the four bytes in each column.
    out = out.xor_round_key(keys, i + 1) # 4. AddRoundKey
  end
  # Final Round (no MixColumns)
  out = out.sub_bytes # 1. SubBytes
  out = out.shift_rows # 2. ShiftRows
  out.xor_round_key(keys, 10) # 3. AddRoundKey
end

# @param [message in bytes] m
# @param [special key in bytes] keys
def aes_decrypt_128(m, keys)

  out = m.xor_round_key(keys, 10)
  out = out.shift_rows_inv
  out = out.sub_bytes_inv
  9.times do |i|
    out = out.xor_round_key(keys, 9 - i)
    out = out.mix_cols_inv
    out = out.shift_rows_inv
    out = out.sub_bytes_inv
  end
  out.xor_round_key(keys, 0)
end

